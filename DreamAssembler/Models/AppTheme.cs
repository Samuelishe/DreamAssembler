namespace DreamAssembler.App.Models;

/// <summary>
/// Определяет доступные темы оформления приложения.
/// </summary>
public enum AppTheme
{
    /// <summary>
    /// Теплая светлая тема.
    /// </summary>
    WarmLight,

    /// <summary>
    /// Светлая тема с прохладным бумажным оттенком.
    /// </summary>
    MistLight,

    /// <summary>
    /// Графитовая темная тема.
    /// </summary>
    GraphiteDark,

    /// <summary>
    /// Темная тема с винно-сливовым акцентом.
    /// </summary>
    PlumNight,

    /// <summary>
    /// Светлая дождливая тема с холодным городским оттенком.
    /// </summary>
    RainyDay,

    /// <summary>
    /// Темная тема с архивно-оливковым настроением.
    /// </summary>
    ArchiveDusk,

    /// <summary>
    /// Ночная тема с транспортно-бирюзовым акцентом.
    /// </summary>
    TramNight
}
